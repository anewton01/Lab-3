import turtle
def newturtle(color):
    bob2=turtle.Turtle()
    bob2.color(color)
    bob2.forward(100)
    bob2.right(90)
    bob2.forward(100)
    bob2.right(90)
    bob2.forward(100)
    bob2.right(90)
    bob2.forward(100)


newturtle("cyan")
newturtle("green")
newturtle("blue")
newturtle("gold")
